import math

# Calculate the square root
number = 25
square_root = math.sqrt(number)
print(f"Square root of {number} is {square_root}")

# Calculate the factorial
factorial_num = 5
factorial_result = math.factorial(factorial_num)
print(f"Factorial of {factorial_num} is {factorial_result}")

# Calculate the logarithm base 10
logarithm_result = math.log10(100)
print(f"Logarithm base 10 of 100 is {logarithm_result}")

# Calculate the sine and cosine
angle_in_radians = math.pi / 4
sin_value = math.sin(angle_in_radians)
cos_value = math.cos(angle_in_radians)
print(f"Sine of {angle_in_radians} radians is {sin_value}")
print(f"Cosine of {angle_in_radians} radians is {cos_value}")

# Convert radians to degrees
radians = math.pi
degrees = math.degrees(radians)
print(f"{radians} radians is equal to {degrees} degrees")

# Convert degrees to radians
degrees = 180
radians = math.radians(degrees)
print(f"{degrees} degrees is equal to {radians} radians")

# Calculate the power
base = 2
exponent = 3
power_result = math.pow(base, exponent)
print(f"{base} raised to the power of {exponent} is {power_result}")

# Round a number to the nearest integer
number_to_round = 3.7
rounded_number = math.ceil(number_to_round)
print(f"{number_to_round} rounded up is {rounded_number}")

number_to_round = 3.2
rounded_number = math.floor(number_to_round)
print(f"{number_to_round} rounded down is {rounded_number}")

